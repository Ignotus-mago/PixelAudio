package net.paulhertz.pixelaudio.sampler;

/**
 * Backend engine contract for sampler implementations.
 * Allows swapping implementations without changing higher layers.
 */
public interface PASampler {
	
    /**
     * Play command with all the useful arguments in standard order.
     * 
     * @param samplePos    index into the buffer to start sampling 
     * @param sampleLen    expected duration of the audio event, which is expected to return its actual duration
     * @param amplitude    maximum amplitude of signal generated by the sampler, linear value
     * @param env          ADSR style envelope for the entire event
     * @param pitch        pitch multiplier, where 1.0f implies pitch is unchanged and all samples are read
     * @param pan          location of audio event in stereo field, -1.0f = left, 0.0f = center, 1.0f = right
     * @return             actual duration of event in samples, typically sampleLen + envelope release time
     */
    int play(int samplePos, int sampleLen, float amplitude, ADSRParams env, float pitch, float pan);

    /**
     * Schedules playback at an absolute sample time on this sampler's audio clock.
     *
     * @param samplePos    index into the buffer to start sampling
     * @param sampleLen    expected duration of the audio event
     * @param amplitude    maximum amplitude of signal generated by the sampler, linear value
     * @param env          ADSR style envelope for the entire event
     * @param pitch        pitch multiplier, where 1.0f implies pitch is unchanged
     * @param pan          location of audio event in stereo field, -1.0f = left, 0.0f = center, 1.0f = right
     * @param startSample  absolute sample index on this sampler's clock
     */
    void startAtSampleTime(int samplePos, int sampleLen, float amplitude,
            ADSRParams env, float pitch, float pan, long startSample);

    /**
     * Schedules playback after a delay in samples relative to this sampler's current audio clock.
     *
     * @param samplePos      index into the buffer to start sampling
     * @param sampleLen      expected duration of the audio event
     * @param amplitude      maximum amplitude of signal generated by the sampler, linear value
     * @param env            ADSR style envelope for the entire event
     * @param pitch          pitch multiplier, where 1.0f implies pitch is unchanged
     * @param pan            location of audio event in stereo field, -1.0f = left, 0.0f = center, 1.0f = right
     * @param delaySamples   delay from the current sample time
     */
    void startAfterDelaySamples(int samplePos, int sampleLen, float amplitude,
            ADSRParams env, float pitch, float pan, long delaySamples);

    /**
     * Returns this sampler's current audio-thread sample clock.
     *
     * @return absolute sample cursor maintained by the sampler
     */
    long getCurrentSampleTime();

    /** Clears scheduled starts that have not fired yet. */
    void clearScheduled();

    /**
     * Sets whether new sampler voices wrap source-buffer reads at the buffer end.
     *
     * <p>Wrapping is distinct from looping: a wrapped one-shot still plays for its requested
     * duration and envelope, but sample reads that pass the end of the source buffer continue
     * from the beginning.</p>
     *
     * @param wrapAround true to wrap source-buffer reads for newly triggered voices
     */
    void setWrapAround(boolean wrapAround);

    /**
     * Reports whether new sampler voices wrap source-buffer reads at the buffer end.
     *
     * @return true when source-buffer wrapping is enabled
     */
    boolean isWrapAround();

    /**
     * Returns true if any currently active voice is looping.
     *
     * @return true if one or more voices are looping
     */
    boolean isLooping();

	/** Stops all voices immediately. */
	void stopAll();
	
	/** Releases all voices through their envelopes. */
	void releaseAll();

	/**
	 * Updates the source-buffer playback sample rate.
	 *
	 * <p>This is distinct from the audio device or Minim {@code UGen} sample rate.
	 * It controls how source-buffer sample positions are interpreted for playback speed
	 * and envelope duration calculations.</p>
	 *
	 * @param newRate   new source-buffer playback sample rate in Hz
	 */
	void setPlaybackSampleRate(float newRate);
	
	/**
	 * Replaces the sampler source buffer.
	 *
	 * @param buffer   mono source sample buffer
	 */
	public void setBuffer(float[] buffer);
	
	/**
	 * Replaces the sampler source buffer and playback sample rate.
	 *
	 * @param buffer   mono source sample buffer
	 * @param playbackSampleRate sample rate of the source buffer in Hz
	 */
	public void setBuffer(float[] buffer, float playbackSampleRate);
	
}
