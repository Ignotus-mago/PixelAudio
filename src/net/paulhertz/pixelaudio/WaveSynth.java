	/**
	 * Sets the audio rendering flag.
	 * @param isRenderAudio   true to enable audio rendering, false to disable
	 */
	public void setRenderAudio(boolean isRenderAudio) {
		if (isRenderAudio) {
			this.renderSignal = new float[audioSignal.length];
		}
		this.isRenderAudio = isRenderAudio;
	}

	/**
	 * Legacy code: set up mapImage for editing, load colorSignal, set mapInc.
	 * colorSignal = mapper.pluckPixels(...) is no longer essential, 
	 * because renderFrame() overwrites every colorSignal[pos] before 
	 * planting it back into mapImage.pixels.
	 * 
	 */
	public void prepareAnimation() {
		this.mapImage.loadPixels();
		this.colorSignal = mapper.pluckPixels(mapImage.pixels, 0, mapSize);
		this.mapInc = PConstants.TWO_PI / this.sampleRate;
	}
	
	/**
	 * Loop to render all the pixels in a frame. We want it to complete a frame 
	 * before any changes to the WaveSynth, so it's synchronized.
	 * 
	 * @param frame    the number of the frame we are rendering in an animation sequence
	 * @return a PImage derived from additive audio synthesis.
	 */
	public synchronized PImage renderFrame(int frame) {
		mapImage.loadPixels();
		if (mapInc == 0) mapInc = PConstants.TWO_PI / this.sampleRate;
		// NEW: build active wave list and pre-split their colors
		rebuildActiveWaves();
		// NEW: per-frame oscillator preparation for each active wave
		for (int j = 0; j < activeCount; j++) {
			WaveData wd = activeWaves[j];
			wd.prepareFrame(frame, mapInc);   // uses the new WaveData API
		}
		// Main path along the mapper's signal
		for (int pos = 0; pos < this.mapSize; pos++) {
			float weightSum = 0.0f;
			// accumulate weights for active waves, and optionally audio sum
			for (int j = 0; j < activeCount; j++) {
				WaveData wd = activeWaves[j];
				// Fast oscillator recurrence: no trig here
				float base = wd.nextValue();
				float val  = (base + woff) * wscale + wd.dc;
				float w    = val * wd.amp * this.gain;
				activeWeights[j] = w;
				if (isRenderAudio) {
					weightSum += w;
				}
			}
			if (isRenderAudio) {
				this.renderSignal[pos] = weightSum;
			}
			// Optimized color blend for active waves
			this.colorSignal[pos] = weightedColorActive(activeWeights, activeCount);
		}
		// write scanSignal's pixel color values to scanImage pixels
		this.mapper.plantPixels(colorSignal, mapImage.pixels, 0, mapSize);
		this.mapImage.updatePixels();
		if (isRenderAudio) {
			audioSignal = renderSignal;
		}
		// set our internal step variable, just a tracker for now
		this.setStep(frame);
		return mapImage;
	}
	
    /**
     * Optimized weighted color for the current active waves.
     * Uses pre-split RGB and only the first activeCount entries.
     * Gamma and Histogram settings are primarily intended for still frame rendering. 
     * You can probably tweak operator amplitudes and perhaps DC offset to obtain the 
     * brightness and contrast you want for animation. Leave gamma = 1.0 and 
     * isScaleHisto = false for best performance.
	 * @param weights   the array of weights for scaling the active waves
	 * @param count     the number of active waves (length of valid entries in weights and activeColor arrays)
     */
    private int weightedColorActive(float[] weights, int count) {
        float r = 0, g = 0, b = 0;

        for (int i = 0; i < count; i++) {
            float w = weights[i];
            r += activeColorR[i] * w;
            g += activeColorG[i] * w;
            b += activeColorB[i] * w;
        }

        // gamma correction
        if (this.gamma != 1.0) {
            if (useGammaTable && gammaTable != null) {
                r = PixelAudio.constrain(r, 0, 255);
                g = PixelAudio.constrain(g, 0, 255);
                b = PixelAudio.constrain(b, 0, 255);

                r = gammaTable[(int) r];
                g = gammaTable[(int) g];
                b = gammaTable[(int) b];
            } else {
                r = (float) Math.pow((r / 255.0f), this.gamma) * 255.0f;
                g = (float) Math.pow((g / 255.0f), this.gamma) * 255.0f;
                b = (float) Math.pow((b / 255.0f), this.gamma) * 255.0f;
            }
        }

        // linear stretch, if you want it, adjust hi and lo to suit
        if (this.isScaleHisto) {
            r = PixelAudio.constrain(PixelAudio.map(r, this.histoLow, this.histoHigh, 1, 254), 0, 255);
            g = PixelAudio.constrain(PixelAudio.map(g, this.histoLow, this.histoHigh, 1, 254), 0, 255);
            b = PixelAudio.constrain(PixelAudio.map(b, this.histoLow, this.histoHigh, 1, 254), 0, 255);
        }

        return PixelAudioMapper.composeColor((int) r, (int) g, (int) b, 255);
    }


	
	/**
	 * 	Render one pixel, return its RGB value.
	 *  
	 *  <br><br>NOTES<br>
	 *  Our basic equation:
	 *  <pre>{@code sample amplitude = sin(initial phase + phase shift + frequency * i * (TWO_PI/n))}</pre>
	 *  Restated, in two parts:
	 *  <pre>{@code wd.phaseInc = (wd.cycles * TWO_PI)/animSteps; mapInc = TWO_PI / mapSize;}</pre>
	 *  <pre>{@code float val = (float) (Math.sin(wd.phaseTwoPi - frame * wd.phaseInc + wd.freq * freqShift * pos * mapInc) + woff) * wscale + wd.dc;}</pre>
	 *  Instead of incrementing phase at each step, we subtract (frame * phase increment)
	 *  from the initial phase: instead of adding, we subtract so that animation data files 
	 *  give the same result in previous implementations. And yes, I have forgotten the 
	 *  original reasons for subtracting.
	 *  <p>
	 *  We now let the WaveData object calculate the signal: this is much more flexible and
	 *  barely affects the time. For the latest version (2026-06), the WaveData class has been
	 *  refactored to have a prepareFrame() method that calculates the current phase for the
	 *  frame, and a nextValue() method that uses recurrence relations to calculate the next
	 *  sample value without trig functions. This should be much more efficient, especially for
	 *  large numbers of waves.
	 *  </p>
	 * 
	 * @param frame     number of the frame to render
	 * @param pos       index position of the pixels
	 * @param wdList    list of WaveData objects that calculate output value
	 * @return          the color of the pixel in RGBA format
	 */
	public int renderPixel(int frame, int pos, ArrayList<WaveData> wdList) {
		// noisiness is an experiment, left for future development
		// float freqShift = noisiness != 0.0f ? noiseAt(pos % mapper.width, (int) Math.floor(pos / mapper.width)) : 1;
	    // if (pos == 0) System.out.println("==> freqShift = "+ freqShift);
		for (int j = 0; j < dataLength; j++) {
			WaveData wd = waveDataList.get(j);
			// TODO this logic has a performance hit. Move it out of here.
			if (wd.isMuted || wd.waveState == WaveData.WaveState.SUSPENDED) continue;
			// float val = (wd.waveValue(frame, pos, freqShift, mapInc) + woff) * wscale + wd.dc;
			float val = (wd.waveValue(frame, pos, mapInc) + woff) * wscale + wd.dc;
			weights[j] = val * wd.amp * this.gain;
		}
		if (isRenderAudio) {
			float weightSum = 0.0f;
			for (int i = 0; i < weights.length; i++) {
				weightSum += weights[i];
			}
			this.renderSignal[pos] = weightSum;
		}
		return this.weightedColor(waveColors, weights);
	}
	
	/**
	 * Calculate the weighted color for a pixel based on the colors and weights of the active waves.
	 * This method applies optional gamma correction and optional histogram scaling to the resulting color,
	 * but you should avoid using those for animation, as they are primarily intended for still frame rendering.
	 * 
	 * @param colors     an array of colors 
	 * @param weights    an array of weights for scaling the colors
	 * @return           weighted color value
	 */
	public int weightedColor(int[] colors, float[] weights) {
		float r = 0, g = 0, b = 0;
		for (int i = 0; i < colors.length; i++) {
			int[] comp = PixelAudioMapper.rgbComponents(colors[i]);
			r += comp[0] * weights[i];
			g += comp[1] * weights[i];
			b += comp[2] * weights[i];
		}
		// gamma correction
		if (this.gamma != 1.0) {
			if (useGammaTable) {
				r = PixelAudio.constrain(r, 0, 255);
				r = gammaTable[(int)r];
				g = PixelAudio.constrain(g, 0, 255);
				g = gammaTable[(int)g];
				b = PixelAudio.constrain(b, 0, 255);
				b = gammaTable[(int)b];
			}
			else {
				r = (float) Math.pow((r / 255.0), this.gamma) * 255;
				g = (float) Math.pow((g / 255.0), this.gamma) * 255;
				b = (float) Math.pow((b / 255.0), this.gamma) * 255;
			}
		}
		// linear stretch, if you want it, adjust hi and lo to suit
		if (this.isScaleHisto) {
			r = PixelAudio.constrain(PixelAudio.map(r, this.histoLow, this.histoHigh, 1, 254), 0, 255);
			g = PixelAudio.constrain(PixelAudio.map(g, this.histoLow, this.histoHigh, 1, 254), 0, 255);
			b = PixelAudio.constrain(PixelAudio.map(b, this.histoLow, this.histoHigh, 1, 254), 0, 255);
		}
		return PixelAudioMapper.composeColor((int) r, (int) g, (int) b, 255);
	}	
	
	/**
	 * Render the audio signal for a given frame, returning an array of float values representing the audio signal.
	 * This method builds the active wave list, prepares the oscillators for the current frame,
	 * and sums the contributions of the active waves to produce the audio signal. The resulting 
	 * audio signal is normalized to a specified limit.
	 * @param frame    the frame to render
	 * @return         audio signal as an array of float, scaled by 0.95f normalization
	 */
	public float[] renderAudio(int frame) {
		return this.renderAudio(frame, 0.95f);
	}
	
	
	/**
	 * Render the raw audio signal for a given frame, returning an array of float values representing the audio signal.
	 * This method builds the active wave list, prepares the oscillators for the current frame,
	 * and sums the contributions of the active waves to produce the audio signal. It is a little
	 * more efficient than renderAudio() because it does not normalize the signal.
	 * @param frame   the frame to render
	 * @return        the raw audio signal, rendered but not normalized
	 */
	public float[] renderAudioRaw(int frame) {
	    // Build active list and prepare oscillators for this frame
	    rebuildActiveWaves();
	    for (int j = 0; j < activeCount; j++) {
	        WaveData wd = activeWaves[j];
	        wd.prepareFrame(frame, mapInc);
	    }
	    // Walk the path, sum active waves into audioSignal
	    for (int pos = 0; pos < this.mapSize; pos++) {
	        float weightSum = 0.0f;
	        for (int j = 0; j < activeCount; j++) {
	            WaveData wd = activeWaves[j];
	            float base = wd.nextValue();
	            float val  = (base + woff) * wscale + wd.dc;
	            float w    = val * wd.amp * this.gain;
	            weightSum += w;
	        }
	        this.audioSignal[pos] = weightSum;
	    }
	    return audioSignal;
	}

	/**
	 * Render the audio signal for a given frame, returning an array of float values representing the 
	 * normalized audio signal. This method calls renderAudioRaw() to get the raw audio signal, and 
	 * then normalizes it to a specified limit using the normalize() method. The resulting audio signal 
	 * is suitable for output to an audio file or audio output device.
	 * 
	 * @param frame    the frame to render
	 * @param limit    the normalization limit
	 * @return         audio signal, rendered and normalized
	 */
    public float[] renderAudio(int frame, float limit) {
        renderAudioRaw(frame);
        return WaveSynth.normalize(audioSignal, limit);
    }
	
