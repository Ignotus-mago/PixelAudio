	/**
	 * Normalize an array of float values to a specified limit. This method finds the minimum and maximum values in the input array,
	 * and then maps all values to a new range defined by -limit to limit. 
	 * 
	 * @param sig      the array of float values representing the audio signal to be normalized
	 * @param limit    the maximum absolute value for the normalized signal
	 * @return an array of float values representing the normalized audio signal
	 */
	public static float[] normalize(float[] sig, float limit) {
		float min = 0;
		float max = 0;
		for (int i = 0; i < sig.length; i++) {
			if (sig[i] < min) min = sig[i];
			if (sig[i] > max) max = sig [i];
		}
		for (int i = 0; i < sig.length; i++) {
			sig[i] = PixelAudio.map(sig[i], min, max, -limit, limit);
		}
		return sig;
	}

	/**
	 * Normalize an array of float values to a limit of 1.0f.
	 * @param sig    the array of float values representing the audio signal to be normalized
	 * @return an array of float values representing the normalized audio signal
	 */
	public static float[] normalize(float[] sig) {
		return normalize(sig, 1.0f);
	}

	/**
	 * Calculate the histogram bounds (minimum and maximum pixel values) for a given array of pixel values. 
	 * This method iterates through the input array, extracts the RGB components of each pixel, and updates 
	 * the minimum and maximum values found. The resulting minimum and maximum values can be used for histogram 
	 * scaling or other image processing tasks. May not produce the desired contrast 
	 * enhancement if the histogram is heavily skewed.
	 * 
	 * @param source    array of pixel values to analyze for min and max
	 * @return          int[] array with min and max values
	 */
	public static int[] getHistoBounds(int[] source) {
	    int min = 255;
	    int max = 0;
	    for (int i = 0; i < source.length; i++) {
	      int[] comp = PixelAudioMapper.rgbComponents(source[i]);
	      for (int j = 0; j < comp.length; j++) {
	        if (comp[j] > max) max = comp[j];
	        if (comp[j] < min) min = comp[j];
	      }
	    }
	    return new int[]{min, max};
	}

