	/**
	 * Sets the PixelAudioMapper for this WaveSynth and initializes associated properties and data structures.
	 * @param mapper
	 */
	public void setMapper(PixelAudioMapper mapper) {
		this.mapper = mapper;
		this.w = mapper.getWidth();
		this.h = mapper.getHeight();
		this.mapSize = w * h;
		this.sampleRate = mapSize;
		this.mapImage = PixelAudio.myParent.createImage(w, h, PConstants.RGB);
		this.colorSignal = new int[mapSize];
		this.audioSignal = new float[mapSize];
	}
	
	/**
	 * Sets the list of WaveData objects for this WaveSynth.
	 * @param wdList    list of WaveData objects to initialize local waveDataList with
	 */
	public void setWaveData(ArrayList<WaveData> wdList) {
		this.waveDataList = wdList;
		this.dataLength = wdList.size();
		this.waveColors = new int[dataLength];
		for (int j = 0; j < dataLength; j++) {
			waveColors[j] = waveDataList.get(j).waveColor;
		}
		this.weights = new float[dataLength];
        // ***** NEW: allocate optimized arrays ***** //
        if (this.activeWaves == null || this.activeWaves.length != dataLength) {
            this.activeWaves = new WaveData[dataLength];
        }
        if (this.activeWeights == null || this.activeWeights.length != dataLength) {
            this.activeWeights = new float[dataLength];
        }
        if (this.activeColorR == null || this.activeColorR.length != dataLength) {
            this.activeColorR = new float[dataLength];
            this.activeColorG = new float[dataLength];
            this.activeColorB = new float[dataLength];
        }		
	}
	
	/**
	 * Updates the wave colors based on the current list of WaveData objects.
	 */
	public void updateWaveColors() {
		this.dataLength = this.waveDataList.size();
		if (this.waveColors == null || this.waveColors.length != this.dataLength) {
			this.waveColors = new int[dataLength];
			this.weights = new float[dataLength];
			// ***** Also resize optimized arrays if needed ***** //
            this.activeWaves    = new WaveData[dataLength];
            this.activeWeights  = new float[dataLength];
            this.activeColorR   = new float[dataLength];
            this.activeColorG   = new float[dataLength];
            this.activeColorB   = new float[dataLength];
		}
		for (int j = 0; j < dataLength; j++) {
			waveColors[j] = waveDataList.get(j).waveColor;
		}
	}
	
	// ***** NEW optimized synthesis ***** //
	/**
	 * Rebuilds the list of active (unmuted, non-suspended) waves
	 * and pre-splits their colors into RGB channels.
	 */
	private void rebuildActiveWaves() {
	    if (waveDataList == null) {
	        activeCount = 0;
	        return;
	    }
	    int idx = 0;
	    for (int j = 0; j < waveDataList.size(); j++) {
	        WaveData wd = waveDataList.get(j);
	        if (wd.isMuted || wd.waveState == WaveData.WaveState.SUSPENDED) {
	            continue;
	        }
	        activeWaves[idx] = wd;
	        // Pre-split color into RGB for this active index
	        int c = wd.waveColor;
	        int[] comp = PixelAudioMapper.rgbComponents(c);
	        activeColorR[idx] = comp[0];
	        activeColorG[idx] = comp[1];
	        activeColorB[idx] = comp[2];
	        idx++;
	    }
	    activeCount = idx;
	}
	
	/**
	 * Initializes a list of WaveData for use by a WaveSynth.
	 * 
	 * @return an ArrayList of WaveData objects
	 */
	public static ArrayList<WaveData> quickWaveDataList() {
		ArrayList<WaveData> list = new ArrayList<WaveData>();
		float frequency = 768.0f;
		float amplitude = 0.8f;
		float phase = 0.0f;
		float dc = 0.0f;
		float cycles = 1.0f;
		int waveColor = PixelAudioMapper.composeColor(159, 190, 251);
		int steps = 240;
		WaveData wd = new WaveData(frequency, amplitude, phase, dc, cycles, waveColor, steps);
		list.add(wd);
		frequency = 192.0f;
		phase = 0.0f;
		cycles = 2.0f;
		waveColor = PixelAudioMapper.composeColor(209, 178, 117);
		wd = new WaveData(frequency, amplitude, phase, dc, cycles, waveColor, steps);
		list.add(wd);
		return list;
	}


