	/**
	 * Apply a linear stretch to an array of pixel values based on specified low and high bounds. 
	 * This method iterates through the input array, extracts the RGB components of each pixel, 
	 * and applies a linear mapping to stretch the values from the range defined by low and high 
	 * to the full range of 0 to 255. The resulting array contains the stretched pixel values, 
	 * which can be used for image enhancement or other processing tasks. 
	 * 
	 * You can run getHistoBounds to determine low and high values for the stretch.
	 * Not recommended for animation.
	 * 
	 * @param source    an array of pixel values to stretch
	 * @param low       desired lower bound 
	 * @param high      desired upper bound
	 * @return          scaled array of pixel values
	 */
	public static int[] stretch(int[] source, int low, int high) {
	  int[] out = new int[source.length];
	  int r = 0, g = 0, b = 0;
	  for (int i = 0; i < out.length; i++) {
	    int[] comp = PixelAudioMapper.rgbComponents(source[i]);
	    r = comp[0];
	    g = comp[1];
	    b = comp[2];
	    r = (int) PixelAudio.constrain(PixelAudio.map(r, low, high, 0, 255), 0, 255);
	    g = (int) PixelAudio.constrain(PixelAudio.map(g, low, high, 0, 255), 0, 255);
	    b = (int) PixelAudio.constrain(PixelAudio.map(b, low, high, 0, 255), 0, 255);
	    out[i] = PixelAudioMapper.composeColor(r, g, b, 255);
	  }
	  return out;
	}

	
